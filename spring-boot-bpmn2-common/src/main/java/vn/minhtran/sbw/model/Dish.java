package vn.minhtran.sbw.model;

public class Dish {

	private String name;
	private int price;

	public String getName() {
		return this.name;
	}
	public int getPrice() {
		return this.price;
	}
	public void setName(final String name) {
		this.name = name;
	}
	public void setPrice(final int price) {
		this.price = price;
	}
}
